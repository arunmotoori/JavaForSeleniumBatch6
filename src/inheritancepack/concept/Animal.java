package inheritancepack.concept;

public class Animal {
	
	boolean pet;
	boolean tail;
	boolean fly;
	String color;
	int legs;
	
	public void eat() {
		System.out.println("Animal is eating");
	}
	
	public void sleep() {
		System.out.println("Animal is sleeping");
	}
	
	public void run() {
		System.out.println("Animal is running");
	}
	
	public void climb() {
		System.out.println("Animal is climbing");
	}
	
	public void hunt() {
		System.out.println("Animal is hunting");
	}

}
