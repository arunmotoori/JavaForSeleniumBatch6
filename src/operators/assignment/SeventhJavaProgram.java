package operators.assignment;

public class SeventhJavaProgram {

	public static void main(String[] args) {
		
		int a = 5; // Declaring and Initializing the variable
		System.out.println(a);  // 5
		
		int b = 5; // Initializing
		b = 10; // Updating or Re-initializing
		System.out.println(b); // 10
		
		int c;  // Declaring the variable
		c = 5; // Initializing the variable
		System.out.println(c);
		
		int d=3,e=4,f=5,g=6,h=7;
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
		
		int i,j,k,l,m;
		i = j = k = l = m = 9;
		System.out.println(i);
		System.out.println(j);
		System.out.println(k);
		System.out.println(l);
		System.out.println(m);
		
		int n;
		n = 9;
		System.out.println(n);
		
	}

}
