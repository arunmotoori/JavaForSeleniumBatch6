package operators.arithmetic;

public class ThirdJavaProgram {
	
	

	public static void main(String[] args) {
		
		int a = 5;
		int b = 3;
		
		System.out.println(a+b); // 8
		System.out.println(a-b); // 2
		System.out.println(a*b); // 15
		System.out.println(a/b); // 1
		System.out.println(a%b); // 2
		
	}

}
