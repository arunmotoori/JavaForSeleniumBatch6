package arrayspack.single;

public class SampleSeven {

	public static void main(String[] args) {

		// Declaring an Array
		int[] ar;
		
		//Creating an Array
		ar = new int[4];

		// Initializing Array elements
		ar[0] = 9;
		ar[1] = 5;
		ar[2] = 7;
		ar[3] = 6;

		// Accessing Array elements
		System.out.println(ar[0]); // 9
		System.out.println(ar[1]); // 5
		System.out.println(ar[2]); // 7
		System.out.println(ar[3]); // 6

	}

}
