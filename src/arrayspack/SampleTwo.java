package arrayspack;

public class SampleTwo {

	public static void main(String[] args) {

		// Creating Array and initialize Array elements - shortcut representation of Array
		int[] ar = {9,5,7,6};

		// Accessing Array elements
		System.out.println(ar[0]); // 9
		System.out.println(ar[1]); // 5
		System.out.println(ar[2]); // 7
		System.out.println(ar[3]); // 6

	}

}
