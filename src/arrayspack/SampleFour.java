package arrayspack;

public class SampleFour {

	public static void main(String[] args) {

		int[] ar = { 9, 5, 7, 6, 2, 4, 8, 1, 3, 0, 13, 24, 42, 28, 26, 14, 55};
		
		System.out.println(ar.length);
		System.out.println("-----------");

		for (int i = 0; i < ar.length; i++) {
			System.out.println(ar[i]); // 9 5 7 6 2 4 8 1 3
		}

	}

}
